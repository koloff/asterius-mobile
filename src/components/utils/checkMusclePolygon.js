function convertCoordinatesToArray(coordStr) {
  let splitted = coordStr.split(',');
  let arr = [];

  for (let i = 0; i < splitted.length; i++) {
    arr.push([splitted[i], splitted[i + 1]]);
    i++;
  }

  return arr;
}


